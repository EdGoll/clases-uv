


public class Jugador {

    private int id;
    private int BR;
    private int BG;
    private int GC;
    private String tipo;
    private int nj_invididual;
    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getBR() {
        return BR;
    }

    public void setBR(int BR) {
        this.BR = BR;
    }

    public int getBG() {
        return BG;
    }

    public void setBG(int BG) {
        this.BG = BG;
    }

    public int getGC() {
        return GC;
    }

    public void setGC(int GC) {
        this.GC = GC;
    }       

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public int getNj_invididual() {
        return nj_invididual;
    }

    public void setNj_invididual(int nj_invididual) {
        this.nj_invididual = nj_invididual;
    }
}
