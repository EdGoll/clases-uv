


public class Encuentro {
    private int e_local;
    private int e_visita;
    private int e_ganador;

    public int getE_local() {
        return e_local;
    }

    public void setE_local(int e_local) {
        this.e_local = e_local;
    }

    public int getE_visita() {
        return e_visita;
    }

    public void setE_visita(int e_visita) {
        this.e_visita = e_visita;
    }

    public int getE_ganador() {
        return e_ganador;
    }

    public void setE_ganador(int e_ganador) {
        this.e_ganador = e_ganador;
    }
    
}
