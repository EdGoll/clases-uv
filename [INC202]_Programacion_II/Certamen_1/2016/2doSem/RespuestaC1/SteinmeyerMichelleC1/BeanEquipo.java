public class BeanEquipo {

    private int ID;
    private int JC; // Juego colectivo
    private String Listado; 
    
    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public int getJC() {
        return JC;
    }

    public void setJC(int JC) {
        this.JC = JC;
    }

    public String getListado() {
        return Listado;
    }

    public void setListado(String Listado) {
        this.Listado = Listado;
    }
}
