public class BeanJugador {
    private int ID;
    private int BR;
    private int BG;
    private int GC;
    private String Tipo;
    
    public int getId() {
        return ID;
    }

    public void setId(int id) {
        this.ID = id;
    }

    public int getBR() {
        return BR;
    }

    public void setBR(int BR) {
        this.BR = BR;
    }

    public int getBG() {
        return BG;
    }

    public void setBG(int BG) {
        this.BG = BG;
    }

    public int getGC() {
        return GC;
    }

    public void setGC(int GC) {
        this.GC = GC;
    }

    public String getTipo(){
        return Tipo;
    }
    
    public void setTipo(String Tipo){
        this.Tipo = Tipo;
    }
    
    
}
