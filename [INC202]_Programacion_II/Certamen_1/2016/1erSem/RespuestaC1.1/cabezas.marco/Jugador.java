public class Jugador {
	private String nombre;
	private String apellido;
	private String region;
	private String telefono;
	private String eloNacional;
	private	String eloFide;	
	private String edad;
	private	String torneos;

	public String getApellido(){
		return apellido;
	}	
	public void setApellido(String apellido){
		this.apellido= apellido;
	}
	public String getNombre(){
		return nombre;
	}	
	public void setNombre(String nombre){
		this.nombre= nombre;
	}
	public String getRegion(){
		return region;
	}	
	public void setRegion(String region){
		this.region= region;
	}
	public String getTelefono(){
		return telefono;
	}	
	public void setTelefono(String telefono){
		this.telefono= telefono;
	}
	public String getEloNacional(){
		return eloNacional;
	}	
	public void setEloNacional(String eloNacional){
		this.eloNacional= eloNacional;
	}
	public String getEloFide(){
		return eloFide;
	}	
	public void setEloFide(String eloFide){
		this.eloFide= eloFide;
	}
	public String getEdad(){
		return Edad;
	}	
	public void setEdad(String edad){
		this.edad= edad;
	}
	public String getTorneos(){
		return torneos;
	}	
	public void setTorneos(String torneos){
		this.torneos= torneos;
	}
}



