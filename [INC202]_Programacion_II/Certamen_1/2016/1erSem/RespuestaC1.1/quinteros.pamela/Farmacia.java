package main;


public class Farmacia {
    private String registro;
    private String nom_producto;
    private String razon_titular;
    private String condicion_venta;

    public String getRegistro() {
        return registro;
    }

    public void setRegistro(String registro) {
        this.registro = registro;
    }

    public String getNom_producto() {
        return nom_producto;
    }

    public void setNom_producto(String nom_producto) {
        this.nom_producto = nom_producto;
    }

    public String getRazon_titular() {
        return razon_titular;
    }

    public void setRazon_titular(String razon_titular) {
        this.razon_titular = razon_titular;
    }

    public String getCondicion_venta() {
        return condicion_venta;
    }

    public void setCondicion_venta(String condicion_venta) {
        this.condicion_venta = condicion_venta;
    }
    
    
    
}
