
package notasalumnos;


public class Alumno {
    private String id;
    private String aP;
    private String aM;
    private String name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getaP() {
        return aP;
    }

    public void setaP(String aP) {
        this.aP = aP;
    }

    public String getaM() {
        return aM;
    }

    public void setaM(String aM) {
        this.aM = aM;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}