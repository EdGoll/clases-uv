package certamen;

public class Alumnos 
{
    private String id;
    private String ap;
    private String am;
    private String nom;
    
    public void setid(String id)
    {
        this.id = id;
    }
    
    public void setap(String ap)
    {
        this.ap = ap;
    }
    
    public void setam(String am)
    {
        this.am = am;
    }
    
    public void setnom(String nom)
    {
        this.nom = nom;
    }
    
    public String getid()
    {
        return id;
    }
    
    public String getap()
    {
        return ap;
    }
    
    public String getam()
    {
        return am;
    }
    
    public String getnom()
    {
        return nom;
    }
}